export function getProductBySlug(slug: string) {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string) {
  return products.find((p) => p.id === id);
}

export function getProductBySku(sku: string) {
  return products.find((p) => p.sku === sku);
}

export function getFeaturedProducts() {
  return products.filter((p) => p.featured);
}

export function getProductsByCategory(category: ProductCategory | string) {
  return products.filter(
    (p) =>
      p.category === category ||
      p.subcategory.toLowerCase() === category.toLowerCase(),
  );
}

export function getProductsByProcess(stage: ProcessStage | string) {
  const normalized = stage.toLowerCase() as ProcessStage;
  return products.filter((p) => p.processStages.includes(normalized));
}

export function getProductsByTechnique(technique: Technique | string) {
  const normalized = technique.toLowerCase() as Technique;
  return products.filter((p) => p.techniques.includes(normalized));
}

export function searchProducts(query: string) {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return products.filter((p) => {
    const haystack = [
      p.title,
      p.sku,
      String(p.supplierSku),
      p.category,
      p.subcategory,
      p.sizeLabel,
      p.materialLabel,
      ...p.processStages,
      ...p.techniques,
      ...p.materials,
      ...p.searchKeywords,
      ...p.packageContents,
      String(p.diameter ?? ""),
      String(p.capacity ?? ""),
    ]
      .join(" ")
      .toLowerCase();
    return haystack.includes(q);
  });
}

export function getRelatedProducts(product: Product, limit = 4) {
  const related = product.relatedProductIds
    .map(getProductById)
    .filter((p): p is Product => Boolean(p));
  if (related.length >= limit) return related.slice(0, limit);
  const extras = products.filter(
    (p) =>
      p.id !== product.id &&
      !related.some((r) => r.id === p.id) &&
      (p.category === product.category ||
        p.processStages.some((s) => product.processStages.includes(s))),
  );
  return [...related, ...extras].slice(0, limit);
}
